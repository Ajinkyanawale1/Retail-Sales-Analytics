---8.TOP 5 PRODUCTS PER REGION
--This shows best selling products in each region.

SELECT * FROM (
SELECT C.REGION,
P.PRODUCT_NAME,
SUM(F.SALES) AS TOTAL_SALES,
RANK() OVER(PARTITION BY C.REGION 
ORDER BY SUM(F.SALES) DESC) AS RANKING
FROM FACT_SALES F
JOIN DIM_CUSTOMERS C
   ON F.Customer_ID=C.Customer_ID
JOIN DIM_PRODUCTS P
   ON F.Product_ID=P.PRODUCT_ID
GROUP BY C.REGION,P.PRODUCT_NAME
)A
WHERE RANKING<=5;

--9.YEAR-OVER-YEAR SALES GROWTH

SELECT 
YEAR(ORDER_DATE) AS YEAR,
FLOOR(SUM(SALES)) AS YEARLY_SALES,
LAG(FLOOR(SUM(SALES))) OVER(ORDER BY YEAR(ORDER_DATE)) AS PREVIOUS_YEAR_SALES,
FLOOR(SUM(SALES))-LAG(FLOOR(SUM(SALES))) OVER(ORDER BY YEAR(ORDER_DATE)) AS SALES_GROWTH
FROM FACT_SALES
GROUP BY YEAR(ORDER_DATE);


--9.CUSTOMER LIFETIME VALUE WITH >100000
SELECT P.CUSTOMER_NAME,
FLOOR(SUM(F.SALES)) AS CUSTOMER_LIFETIME_VALUE
FROM FACT_SALES F
JOIN DIM_CUSTOMERS P
ON P.Customer_ID=F.Customer_ID
GROUP BY Customer_Name
HAVING SUM(SALES)>100000
ORDER BY CUSTOMER_LIFETIME_VALUE DESC


--10.RUNNING SALES TOTAL(CUMULATIVE SALES)

SELECT ORDER_DATE,
SUM(SALES) AS DAILY_SALES,
SUM(SUM(SALES)) OVER (ORDER BY ORDER_DATE) AS RUNNING_TOTAL
FROM FACT_SALES
GROUP BY ORDER_DATE
ORDER BY ORDER_DATE

--11.SALES CONTRIBUTION %
SELECT 
P.CATEGORY,FLOOR(SUM(F.SALES)) AS TOTAL_SALES,
SUM(F.SALES)*100.0/SUM(SUM(F.SALES)) OVER() AS SALES_PERCENTAGE
FROM FACT_SALES F
JOIN DIM_PRODUCTS P
ON F.PRODUCT_ID=P.PRODUCT_ID 
GROUP BY P.CATEGORY


