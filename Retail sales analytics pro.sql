--6.TOP CUSTOMERS (customers who generate highest revenue.)
SELECT  TOP 10 D.CUSTOMER_NAME,
SUM(F.SALES) AS HIGHEST_REVENUE
FROM FACT_SALES F
JOIN DIM_CUSTOMERS D
ON D.Customer_ID=F.Customer_ID
GROUP BY D.Customer_Name
ORDER BY HIGHEST_REVENUE DESC


--7.TOP PRODUCTS (products generating maximum revenue.)

SELECT TOP 10 
P.PRODUCT_NAME,
SUM(F.SALES)TOP_SALES
FROM FACT_SALESSS F
